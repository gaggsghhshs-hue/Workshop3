public class LiteralPractice {
    public static void main(String[] args) {

        long bigNumber = 500000L;     
        float decimalValue = 10.55f;      
        char symbol = '\uDC99';          

        System.out.println("Long value: " + bigNumber);
        System.out.println("Float value: " + decimalValue);
        System.out.println("Unicode char value: " + symbol);
    }
}
